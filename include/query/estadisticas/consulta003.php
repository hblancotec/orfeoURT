<?php
$queryE = "SELECT	convert(varchar(15), C.RADI_NUME_SAL) AS RADICADO,".
					$db->conn->SQLDate('Y-m-d', 'C.SGD_RENV_FECH')." AS FECHA_DE_ENVIO,
					F.SGD_FENV_DESCRIP	AS MEDIO_DE_ENVIO,
					U.USUA_NOMB			AS USUARIO_QUE_ENVIA,
					C.SGD_RENV_PLANILLA AS PLANILLA,
					S.SGD_DEVE_DESC		AS TIPO_DE_DEVOLUCION,
