					C.SGD_RENV_CODIGO	AS HID_CODIGO_ENVIO
			FROM    RADICADO R
					JOIN SGD_RENV_REGENVIO C ON
						C.RADI_NUME_SAL = R.RADI_NUME_RADI
					JOIN SGD_FENV_FRMENVIO F ON
						F.SGD_FENV_CODIGO = C.SGD_FENV_CODIGO
						$wFormEnv
